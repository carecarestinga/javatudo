package com.caretronics.model;

public class Boletim {

	private String escola;
	private Integer ano;
	
	public Boletim() {
	}

	public String getEscola() {
		return escola;
	}

	public void setEscola(String escola) {
		this.escola = escola;
	}

	public Integer getAno() {
		return ano;
	}

	public void setAno(Integer ano) {
		this.ano = ano;
	}

}
